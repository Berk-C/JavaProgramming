package day06_PrimitiveTypeCasting;

public class BirthDay {
    public static void main(String[] args) {

        String firstName= "Berk";
        int day  = 22;
        String birthMonth = "May";
        int birthYear = 2000;

        System.out.println(firstName + " was born on " + birthMonth + "/" + day + "/" + birthYear + "." );


    }
}
